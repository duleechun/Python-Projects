# Get user input
name = input("Enter your name: ")
age = int(input("Enter your age: "))
# Calculate years until 100
years_left = 100 - age 
# Print Result
# Calculate the year the user will turn 100
year_turn_100 = 2024 + years_left
# Print the result
print(f"hello, {name} ! You will turn 100 in {years_left} years ")
print(f"That means you will turn 100 in the year {year_turn_100}")